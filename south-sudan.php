                    <footer><h3>Famine vs. Catastrophe</h3>

                    <p>From February until May 2017, thousands of people in two counties in South Sudan experienced famine. In June, FEWS NET declared that the country&#8217;s entire population had dropped below the threshold for famine. But it also noted substantial increases in the numbers of people experiencing food emergencies and catastrophes.</p>

                    <p>Three precise standards must be met for famine to have occurred in a given area:</p>

                    <ol><li>1. At least 20 percent of households experience an extreme lack of food.</li>
                    <li>2. More than 30 percent of people experience acute malnutrition.</li>
                    <li>3. At least two people per 10,000 die each day.</li></ol>

                    <p>Famine applies to a geographic area, and catastrophe applies to households in an area. A catastrophe can occur without famine when acute malnutrition and increased mortality affect less than 20 percent of households.</p>


